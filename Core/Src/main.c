static uint8_t TelemetryChecksum(const uint8_t *data, uint16_t len)
{
  uint8_t c = 0U;
  for (uint16_t i = 0U; i < len; i++)
  {
    c ^= data[i];
  }
  return c;
}

static uint16_t Crc16CcittFalse(const uint8_t *data, uint16_t len)
{
  uint16_t crc = 0xFFFFU;
  for (uint16_t i = 0U; i < len; i++)
  {
    crc ^= (uint16_t)data[i] << 8;
    for (uint8_t b = 0U; b < 8U; b++)
    {
      if ((crc & 0x8000U) != 0U)
      {
        crc = (uint16_t)((crc << 1) ^ 0x1021U);
      }
      else
      {
        crc <<= 1;
      }
    }
  }
  return crc;
}

static uint16_t U16LE(const uint8_t *p)
{
  return (uint16_t)p[0] | ((uint16_t)p[1] << 8);
}

static void Serial_ProcessVelFrame(const uint8_t *buf)
{
  if (buf[2] != RX_TYPE_VEL)
  {
    return;
  }

  const uint16_t crc_rx = U16LE(&buf[18]);
  const uint16_t crc_ok = Crc16CcittFalse(buf, 18U);
  if (crc_rx != crc_ok)
  {
    return;
  }

  float vx = 0.0f;
  float vy = 0.0f;
  float w = 0.0f;
  memcpy(&vx, &buf[6], sizeof(float));
  memcpy(&vy, &buf[10], sizeof(float));
  memcpy(&w, &buf[14], sizeof(float));

  AppC_WheelSpeeds wheels;
  AppC_RobotToWheels(
      vx,
      vy,
      w,
      0.03f,
      0.09f,
      &wheels);

  const float radps_to_rpm = 9.5492966f;
  AppC_SetCommands(
      wheels.m1 * radps_to_rpm,
      wheels.m2 * radps_to_rpm,
      wheels.m3 * radps_to_rpm,
      wheels.m4 * radps_to_rpm,
      0U);
}

static void Serial_ProcessRx(void)
{
  uint16_t pos = UART_RX_DMA_BUF_SIZE - __HAL_DMA_GET_COUNTER(huart2.hdmarx);

  if (pos == uart_rx_last_pos)
  {
    return;
  }

  if (pos > uart_rx_last_pos)
  {
    for (uint16_t i = uart_rx_last_pos; i < pos; i++)
    {
      Serial_ProcessByte(uart_rx_dma_buf[i]);
    }
  }
  else
  {
    for (uint16_t i = uart_rx_last_pos; i < UART_RX_DMA_BUF_SIZE; i++)
    {
      Serial_ProcessByte(uart_rx_dma_buf[i]);
    }
    for (uint16_t i = 0U; i < pos; i++)
    {
      Serial_ProcessByte(uart_rx_dma_buf[i]);
    }
  }

  uart_rx_last_pos = pos;
}

static void Serial_ProcessByte(uint8_t b)
{
  static uint8_t frame[RX_FRAME_LEN];
  static uint8_t idx = 0U;
  static uint8_t state = 0U;

  switch (state)
  {
    case 0U:
      if (b == RX_SOF0)
      {
        frame[0] = b;
        state = 1U;
      }
      break;

    case 1U:
      if (b == RX_SOF1)
      {
        frame[1] = b;
        idx = 2U;
        state = 2U;
      }
      else if (b == RX_SOF0)
      {
        frame[0] = b;
      }
      else
      {
        state = 0U;
      }
      break;

    case 2U:
      frame[idx++] = b;
      if (idx >= RX_FRAME_LEN)
      {
        Serial_ProcessVelFrame(frame);
        state = 0U;
        idx = 0U;
      }
      break;

    default:
      state = 0U;
      idx = 0U;
      break;
  }
}

static uint8_t Serial_QueueTx(const uint8_t *data, uint16_t len, uint8_t high_prio)
{
  if ((data == NULL) || (len == 0U) || (len >= UART_TX_BUF_SIZE))
  {
    return 0U;
  }

  __disable_irq();
  if (uart_tx_q_count >= UART_TX_QUEUE_DEPTH)
  {
    __enable_irq();
    return 0U;
  }

  uint8_t idx = uart_tx_q_tail;
  if ((high_prio != 0U) && (uart_tx_q_count > 0U))
  {
    idx = (uart_tx_q_head == 0U) ? (UART_TX_QUEUE_DEPTH - 1U) : (uart_tx_q_head - 1U);
    uart_tx_q_head = idx;
  }
  else
  {
    uart_tx_q_tail = (uart_tx_q_tail + 1U) % UART_TX_QUEUE_DEPTH;
  }

  memcpy(uart_tx_queue[idx], data, len);
  uart_tx_queue_len[idx] = len;
  uart_tx_q_count++;
  __enable_irq();

  Serial_TxKick();
  return 1U;
}

static void Serial_TxKick(void)
{
  if ((uart_tx_busy != 0U) || (uart_tx_q_count == 0U))
  {
    return;
  }

  __disable_irq();
  if ((uart_tx_busy != 0U) || (uart_tx_q_count == 0U))
  {
    __enable_irq();
    return;
  }

  const uint8_t idx = uart_tx_q_head;
  const uint16_t len = uart_tx_queue_len[idx];
  memcpy(uart_tx_buf, uart_tx_queue[idx], len);
  uart_tx_q_head = (uart_tx_q_head + 1U) % UART_TX_QUEUE_DEPTH;
  uart_tx_q_count--;
  uart_tx_busy = 1U;
  __enable_irq();

  if (HAL_UART_Transmit_DMA(&huart2, uart_tx_buf, len) != HAL_OK)
  {
    uart_tx_busy = 0U;
  }
}

